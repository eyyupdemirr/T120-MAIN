package day15_nestedForLoop_metodOlusturma;

public class C03_NestedForLoop {
    public static void main(String[] args) {

        for (int i = 1; i <=7; i++) {                           //satir


            for (int k = 1; k <=i ; k++) {                       //sutun

                System.out.print(k+" ");

            }
            System.out.println("");

        }






    }
}
