package day04_dataCasting_WrapperClass;

public class C02_ExplicitNarrowing {
    public static void main(String[] args) {

        double dbl1=130.6;

        int sayi1=(int) dbl1;

        System.out.println("sayi1  =  "+sayi1);

        byte by1=(byte)sayi1;

        System.out.println("by1 =  "+by1);

        int sayi3=260;
        byte by3=(byte)sayi3;
        System.out.println("by3=  "+by3);





    }
}
